/**
 * Helper function to get an icon by name
 * @param {string} name - Icon name
 * @param {string} className - Optional CSS class
 * @param {Object} style - Optional inline styles
 * @returns {string} SVG string
 */
export function getIcon(name, className = '', style = {}) {
    const svg = icons[name];
    if (!svg) return icons.component; // fallback
    
    // Add class and style attributes if provided
    let attributes = '';
    if (className) attributes += ` class="${className}"`;
    if (Object.keys(style).length > 0) {
        const styleStr = Object.entries(style)
            .map(([key, value]) => `${key}: ${value}`)
            .join('; ');
        attributes += ` style="${styleStr}"`;
    }
    
    // Insert attributes into the SVG tag
    return svg.replace('<svg', `<svg${attributes}`);
}

/**
 * Create an icon element
 * @param {string} name - Icon name
 * @param {Object} options - Options for styling
 * @returns {HTMLElement} SVG element
 */
export function createIconElement(name, options = {}) {
    const { size = '16px', color = 'currentColor', className = '' } = options;
    
    const wrapper = document.createElement('span');
    wrapper.innerHTML = getIcon(name, className, {
        width: size,
        height: size,
        color: color
    });
    
    return wrapper.firstElementChild;
}
